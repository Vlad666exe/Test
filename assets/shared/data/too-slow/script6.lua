	precacheImage('greenhillzone/angelisland/deadgrass')
	precacheImage('greenhillzone/angelisland/angelislandtree')
	precacheImage('greenhillzone/angelisland/deadsun')
	precacheImage('greenhillzone/angelisland/gfshadow')
	makeAnimatedLuaSprite('dedpurpr1', 'greenhillzone/angelisland/deadpurpr', -95, 370)
	makeAnimatedLuaSprite('dedpurpr2', 'greenhillzone/angelisland/deadpurpr', 920, 370)
	addAnimationByPrefix('dedpurpr1', 'bouncing', 'bounce', 24, false)
	addAnimationByPrefix('dedpurpr2', 'bouncing', 'bounce', 24, false)
	scaleObject('dedpurpr1', 1.75,1.75)
	scaleObject('dedpurpr2', 1.75,1.75)
	addLuaSprite('dedpurpr1')
	addLuaSprite('dedpurpr2')
